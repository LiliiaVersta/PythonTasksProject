# Определяем рекурсивную функцию для вычисления факториала
def factorial(n):
    if n == 0 or n == 1:  # Базовый случай: факториал 0 и 1 равен 1
        return 1
    else:
        return n * factorial(n - 1)  # Рекурсивный вызов

# Тестируем функцию с разными значениями
for num in [5, 6, 7]:  # Добавлен 7!
    print(f"{num}! = {factorial(num)}")

